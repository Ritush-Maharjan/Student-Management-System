public class Studet {
    private String name;
    private int age;

    // Constructor
    public Studet(String name, int age) {
        this.name = name;
        this.age = age;
    }

    // Getter methods (optional, but useful)
    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    // Setter methods (optional)
    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
